package Simple.daily.practice;

public class Animal {
    public String name;
    public int legs;
    public int count;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLegs() {
        return legs;
    }

    public void setLegs(int legs) {
        this.legs = legs;
    }

    public int getCount() {
        return count;
    }
}
